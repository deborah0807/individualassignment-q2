public class CourseGrad extends Grade {
 
	private double mark;
	private double passf;
	private double finals;

	public CourseGrad() {}

	public void kursus()
	{
		this.mark = mark;
		this.passf = passf;
		this.finals = finals;
	}

	public double getmark()
	{
		return mark;
	}

	public double getpassf()
	{
		return passf;
	}

	public double getfinals()
	{
		return finals;
	}
}